/**
 * Player orientation. Used to provide different player graphics.
 *
 * @author Ullrich Hafner
 */
public enum Orientation {
    /** Player looks to the left. */
    LEFT,
    /** Player looks to the right. */
    RIGHT,
    /** Player looks up. */
    UP,
    /** Player looks down. */
    DOWN
}
